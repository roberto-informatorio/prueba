tamaño = str(input("ingrese tamaño del pez: "))
if tamaño == "normal":
    print("Pez en buenas condiciones")
elif tamaño == "debajo de lo normal":
    print("Pez con problemas de nutricion")
elif tamaño == "encima de lo normal":
    print("Pez con sintomas de organismo contaminado")
elif tamaño == "sobredimensionado":
    print("Pez contaminado")